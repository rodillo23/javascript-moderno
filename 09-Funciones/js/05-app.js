// Diferencia entre parametros y argumentos

function sumar(a,b){ //una funcion recibe parametros
    console.log(a + b);
}

sumar(10, 153) //cuando se manda llamar una funcion se le envian argumentos


function saludar(nombre, apellido){
    console.log(`Hola ${nombre} ${apellido}`);
}

saludar('Rodolfo', 'Figueroa');